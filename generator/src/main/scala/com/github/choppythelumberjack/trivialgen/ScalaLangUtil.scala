package com.github.choppythelumberjack.trivialgen

object ScalaLangUtil {
  def escape(str:String) = {
    if (isKeyword(str)) s"`${str}`" else str
  }

  def isKeyword(word:String) = keywords.contains(word.trim)
  private val keywords = Set("abstract",
    "case",
    "catch",
    "class",
    "def",
    "do",
    "else",
    "extends",
    "false",
    "final",
    "finally",
    "for",
    "forSome",
    "if",
    "implicit",
    "import",
    "lazy",
    "match",
    "new",
    "null",
    "object",
    "override",
    "package",
    "private",
    "protected",
    "return",
    "sealed",
    "super",
    "this",
    "throw",
    "trait",
    "try",
    "true",
    "type",
    "val",
    "var",
    "while",
    "with",
    "yield")
}
